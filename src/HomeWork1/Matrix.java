package HomeWork1;

public class Matrix {
	
	private Integer IDENTITY = 0;
	private Integer HIGH = 0;
	private Integer WIDE = 0;
	
	
	public Integer GET_IDENTITY() {
        return IDENTITY;
    }

    public void SET_IDENTITY(int identity) {
        IDENTITY = identity;
    }

    public Integer GET_HIGH() {
        return HIGH;
    }

    public void SET_HIGH(int high) {
        HIGH = high;
    }

    public Integer GET_WIDE() {
        return WIDE;
    }

    public void SET_WIDE(int wide) {
        WIDE = wide;
    }
    

}
